package diome.java.basico.aula01;

public class Aula04 {

	public static void main(String[] args) {
		
		
		int numeroNegativo = -5;
		
		System.out.println(numeroNegativo);
		
		numeroNegativo = - numeroNegativo;
		
		System.out.println(numeroNegativo);
		
		numeroNegativo = numeroNegativo * -1;
		
		System.out.println(numeroNegativo);
		
		
		int valor = 0;
		
		
		System.out.println(valor);
		
		valor = valor + 1;
		
		System.out.println(valor);
				
		System.out.println(valor++);
		
		System.out.println(valor);
		
		System.out.println(++valor);
		
		System.out.println(valor);
		
		
		valor = valor - 1;
		
		System.out.println(--valor);
		
		System.out.println(valor--);
		
		System.out.println(valor);
	}
}
